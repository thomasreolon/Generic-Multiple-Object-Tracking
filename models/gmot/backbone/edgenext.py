class BackboneEdgenext(nn.Module):
    def __init__(self, initial_downsample=4, n_feat_levels=3, **kwargs):
        super().__init__()

        self.n_feat_levels = n_feat_levels
        depths=[3, 3, 9, 3, 3]
        dims=[32, 64, 100, 192, 256]
        global_block=[0, 1, 1, 1, 1]
        global_block_type=['None', 'SDTA', 'SDTA', 'SDTA', 'SDTA']
        layer_scale_init_value=1e-6
        expan_ratio=4
        kernel_sizes=[3, 5, 7, 9, 5]
        heads=[4, 4, 4, 4, 4]
        use_pos_embd_xca=[False, True, False, False, False]
        d2_scales=[2, 2, 3, 4, 5]

        # initial Downsample
        self.downsample_layer = nn.Sequential(
            nn.Conv2d(3, dims[0], kernel_size=initial_downsample//2, stride=initial_downsample//2),
            LayerNorm(dims[0], eps=1e-6, data_format="channels_first"),
        )

        # Downscales between ViT
        self.feat_levels = nn.ModuleList()  # stem and 3 intermediate downsampling conv layers
        for i in range(n_feat_levels):
            downscale = nn.Sequential(
                LayerNorm(dims[i], eps=1e-6, data_format="channels_first"),
                nn.Conv2d(dims[i], dims[i + 1], kernel_size=2, stride=2),
            )
            self.feat_levels.append(downscale)

        # ViTransformer-like modules
        self.stages = nn.ModuleList()  # 4 feature resolution stages, each consisting of multiple residual blocks
        dp_rates = [x.item() for x in torch.linspace(0, 0., sum(depths))]
        cur = 0
        for i in range(n_feat_levels+1):
            stage_blocks = []
            for j in range(depths[i]):
                if j > depths[i] - global_block[i] - 1:
                    if global_block_type[i] == 'SDTA':
                        stage_blocks.append(SDTAEncoder(dim=dims[i], drop_path=dp_rates[cur + j],
                                                        expan_ratio=expan_ratio, scales=d2_scales[i],
                                                        use_pos_emb=use_pos_embd_xca[i], num_heads=heads[i]))
                    else:
                        raise NotImplementedError
                else:
                    stage_blocks.append(ConvEncoder(dim=dims[i], drop_path=dp_rates[cur + j],
                                                    layer_scale_init_value=layer_scale_init_value,
                                                    expan_ratio=expan_ratio, kernel_size=kernel_sizes[i]))

            self.stages.append(nn.Sequential(*stage_blocks))
            cur += depths[i]


    def _init_weights(self, m):
        if isinstance(m, (nn.Conv2d, nn.Linear)):
            trunc_normal_(m.weight, std=.02)
            if m.bias is not None:
                nn.init.constant_(m.bias, 0)
        elif isinstance(m, (LayerNorm, nn.LayerNorm)):
            nn.init.constant_(m.bias, 0)
            nn.init.constant_(m.weight, 1.0)

    def forward_downscale(self, x):
        # from CHW to C2,H/4,W/4
        x = self.downsample_layer(x)
        x = self.stages[0](x)
        return x

    def forward(self, x):
        x = self.forward_downscale(x)
        features = []

        for i in range(self.n_feat_levels):
            x = self.feat_levels[i](x)
            x = self.stages[i+1](x)
            features.append(x)

        return features



